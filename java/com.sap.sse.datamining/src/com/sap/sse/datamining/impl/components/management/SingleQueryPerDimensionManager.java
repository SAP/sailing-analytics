package com.sap.sse.datamining.impl.components.management;

import java.util.Collection;
import java.util.HashSet;

import com.sap.sse.datamining.Query;
import com.sap.sse.datamining.Query.QueryType;
import com.sap.sse.datamining.functions.Function;
import com.sap.sse.datamining.impl.AdditionalDimensionValuesQueryData;
import com.sap.sse.datamining.impl.components.management.SingleQueryPerDimensionManager.Key;
import com.sap.sse.datamining.shared.DataMiningSession;

public class SingleQueryPerDimensionManager extends SingleQueryPerKeyManager<Key> {

    @Override
    protected <ResultType> Iterable<Key> getKeysFor(DataMiningSession session, Query<ResultType> query) {
        final AdditionalDimensionValuesQueryData additionalData = query.getAdditionalData(AdditionalDimensionValuesQueryData.class);
        if (additionalData == null) {
            throw new IllegalArgumentException("This " + this.getClass().getSimpleName()
                                               + " can only manage queries of the " + QueryType.class.getSimpleName()
                                               + " " + QueryType.DIMENSION_VALUES);
        }
        final Collection<Key> keys = new HashSet<>();
        for (final Function<?> dimension : additionalData.getDimensions()) {
            keys.add(new Key(session, dimension));
        }
        return keys;
    }
    
    static class Key {
        private DataMiningSession session;
        private Function<?> dimension;
        
        public Key(DataMiningSession session, Function<?> dimension) {
            this.session = session;
            this.dimension = dimension;
        }
        
        @Override
        public String toString() {
            return "[session: " + session + ", dimension: " + dimension + "]";
        }

        @Override
        public int hashCode() {
            final int prime = 31;
            int result = 1;
            result = prime * result + ((dimension == null) ? 0 : dimension.hashCode());
            result = prime * result + ((session == null) ? 0 : session.hashCode());
            return result;
        }

        @Override
        public boolean equals(Object obj) {
            if (this == obj)
                return true;
            if (obj == null)
                return false;
            if (getClass() != obj.getClass())
                return false;
            Key other = (Key) obj;
            if (dimension == null) {
                if (other.dimension != null)
                    return false;
            } else if (!dimension.equals(other.dimension))
                return false;
            if (session == null) {
                if (other.session != null)
                    return false;
            } else if (!session.equals(other.session))
                return false;
            return true;
        }
    }
}
