package com.sap.sailing.gwt.ui.client;

import java.util.List;

import com.sap.sailing.domain.common.RegattaIdentifier;

/**
 * Allows UI components to observe a regatta selector, such as a drop-down box showing a list of regattas
 * 
 * @author Axel Uhl (d043530)
 * 
 */
public interface RegattaSelectionChangeListener {

    /**
     * The first element is the first one selected
     * 
     * @param selectedRegattas a non-<code>null</code> list which is empty if nothing is selected
     */
    void onRegattaSelectionChange(List<RegattaIdentifier> selectedRegattas);

}
