    private void setValuesInUI(){
        jRaceID.setText(rpd.getRaceId());
        jStatus.setText(""+rpd.getStatus());
        jDateTime.setText(""+(rpd.getDataTime() == null? "" : dateFormat.format(rpd.getDataTime())));
        jStartTime.setText(""+(rpd.getStartTime()==null?"" :timeFormat.format(rpd.getStartTime())));
        jRaceTime.setText(""+(rpd.getRaceTime()==null? "" :timeFormat.format(rpd.getRaceTime())));
        jNextMarkLeader.setText(""+rpd.getNextMarkLeader());
        jDistanceToNextMarkLeader.setText(""+rpd.getDistanceToNextMarkLeader());
    }
    
    private void getValuesFromUI(){
        rpd.setRaceId(jRaceID.getText());
        rpd.setStatus(Integer.parseInt(jStatus.getText()));
        try {
            rpd.setDataTime(dateFormat.parse(jDateTime.getText()));
            rpd.setStartTime(timeFormat.parse(jStartTime.getText()));
            rpd.setRaceTime(timeFormat.parse(jRaceTime.getText()));
        }
        catch (Exception e){
            
        }
        rpd.setNextMarkLeader(Integer.parseInt(jNextMarkLeader.getText()));
        rpd.setDistanceToNextMarkLeader(Double.parseDouble(jDistanceToNextMarkLeader.getText()));
    }

