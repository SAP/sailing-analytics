package com.sap.sailing.domain.common;

public class DoublePair {
    private final double a;
    private final double b;
    
    public DoublePair(double a, double b) {
        super();
        this.a = a;
        this.b = b;
    }

    public double getA() {
        return a;
    }
    
    public double getB() {
        return b;
    }
}
