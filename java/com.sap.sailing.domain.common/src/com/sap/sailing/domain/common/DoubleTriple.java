package com.sap.sailing.domain.common;

public class DoubleTriple {
    private final double a;
    private final double b;
    private final double c;
    
    public DoubleTriple(double a, double b, double c) {
        super();
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public double getA() {
        return a;
    }
    
    public double getB() {
        return b;
    }
    
    public double getC() {
        return c;
    }
}
