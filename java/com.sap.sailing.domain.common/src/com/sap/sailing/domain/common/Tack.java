package com.sap.sailing.domain.common;

public enum Tack {
    /**
     * The starboard tack means the sails (particularly the main sail) are on the boat's
     * starboard side
     */
    STARBOARD,

    /**
     * The port tack means the sails (particularly the main sail) are on the boat's
     * port side
     */
    PORT
}
