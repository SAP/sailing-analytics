    public static Bitmap scaleCenterCrop(Bitmap source, int newWidth, int newHeight) {
        int sourceWidth = source.getWidth();
        int sourceHeight = source.getHeight();

        // Compute the scaling factors to fit the new height and width,
        // respectively.
        // To cover the final image, the final scaling will be the bigger
        // of these two.
        float xScale = (float) newWidth / sourceWidth;
        float yScale = (float) newHeight / sourceHeight;
        float scale = Math.max(xScale, yScale);

        // Now get the size of the source bitmap when scaled
        float scaledWidth = scale * sourceWidth;
        float scaledHeight = scale * sourceHeight;

        // Let's find out the upper left coordinates if the scaled bitmap
        // should be centered in the new size give by the parameters
        float left = (newWidth - scaledWidth) / 2;
        float top = (newHeight - scaledHeight) / 2;

        // The target rectangle for the new, scaled version of the source bitmap
        // will now
        // be
        RectF targetRect = new RectF(left, top, left + scaledWidth, top + scaledHeight);

        // Finally, we create a new bitmap of the specified size and draw our
        // new,
        // scaled bitmap onto it.
        Bitmap dest = Bitmap.createBitmap(newWidth, newHeight, source.getConfig());
        Canvas canvas = new Canvas(dest);
        canvas.drawBitmap(source, null, targetRect, null);

        return dest;
    }

    public static Bitmap scaleBitmap(Bitmap bitmap, int newWidth, int newHeight) {
        Bitmap scaledBitmap = Bitmap.createBitmap(newWidth, newHeight, Config.ARGB_8888);

        float ratioX = newWidth / (float) bitmap.getWidth();
        float ratioY = newHeight / (float) bitmap.getHeight();
        float middleX = newWidth / 2.0f;
        float middleY = newHeight / 2.0f;

        Matrix scaleMatrix = new Matrix();
        scaleMatrix.setScale(ratioX, ratioY, middleX, middleY);

        Canvas canvas = new Canvas(scaledBitmap);
        canvas.setMatrix(scaleMatrix);
        float left = middleX - (float) bitmap.getWidth() / 2;
        float top = middleY - (float) bitmap.getHeight() / 2;
        canvas.drawBitmap(bitmap, left, top, new Paint(Paint.FILTER_BITMAP_FLAG));
        return scaledBitmap;
    }

    public static Bitmap scaleBitmapAspectRatio(Bitmap bitmap, int targetWidth, int targetHeight) {
        float ratioX = targetWidth / (float) bitmap.getWidth();
        float ratioY = targetHeight / (float) bitmap.getHeight();
        float aspectRatio = (float) bitmap.getWidth() / bitmap.getHeight();
        if (ratioX < ratioY) {
            aspectRatio = ratioX;
        } else {
            aspectRatio = ratioY;
        }
        int newWidth = Math.round(bitmap.getWidth() * aspectRatio);
        if (newWidth < 1) {
            newWidth = 1;
        }
        int newHeight = Math.round(bitmap.getHeight() * aspectRatio);
        if (newHeight < 1) {
            newHeight = 1;
        }
        return scaleBitmap(bitmap, newWidth, newHeight);
    }

